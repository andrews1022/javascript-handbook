// ------------------------------- EXPONENTIAL OPERATOR -------------------------------
// Let's create a function that squares something
// This means x to the power of 2;
const squares = (x) => x ** 2;
squares(2); // 4
squares(3); // 9
squares(4); // 16
squares(5); // 25

// If we want to cube something
const cubed = (y) => y ** 3;
cubed(2); // 8
cubed(3); // 27
cubed(4); // 64
cubed(5); // 125
